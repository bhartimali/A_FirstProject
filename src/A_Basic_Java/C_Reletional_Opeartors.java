package A_Basic_Java;

public class C_Reletional_Opeartors {

	public static void main(String[] args) {
		// Reletional Operator: <,>,<=,>=,==,!=
		int a=10, b=20,c=9;
		System.out.println("a is < b:"+(a<b));
		System.out.println("a is > b:"+(a>b));
		System.out.println("a is >= c:"+(c>=a));
		System.out.println("a is <= b:"+(a<=b));
		System.out.println("a is == b:"+(a==b));
		System.out.println("a is != b:"+(a!=b));
	}

}
